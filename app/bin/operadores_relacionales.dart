void main(List<String> args) {
  /**
   * == es igual
   * != No es igual
   * > Mayor que
   * < Menor que
   * >= Mayor o igual
   * <= Menor o igual
   * OPERADORES LÓGICOS
   * !exp invierte la expresión de false a true o viceversa
   * && AND lógico
   * || OR lógico
   */
  int a = 10, b = 6, c = 10, d = 7;
  var esIgual = a == c;
  var noEsIgual = a != b;
  var mayorQue = a > b;
  var menorQue = b < c;
  print(esIgual);
  print(noEsIgual);
  print(mayorQue);
  print(menorQue);
}
